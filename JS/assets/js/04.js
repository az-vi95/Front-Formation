/*--------- LES OPERATEURS ARITHMETIQUES---------*/

// ##### ~ L'Addition ~ #### //

var nb1, nb2, resultat;
nb1 = 10;
nb2 = 5;

// -- L'Addition de nb1 et nb2 avec l'opérateur " + "
resultat = nb1 + nb2;
console.log('Addition de nb1 et nb2 = ' + resultat)


// #### ~ La Soustraction ~ #### //

// -- La Soustraction de nb1 et nb2 avec l'opérateur " - " 
resultat = nb1 - nb2;
console.log('La Soustraction de nb1 et nb2 = ' + resultat);

// #### ~ La Multiplication ~ #### //

// -- La Multiplication de nb1 et nb2 avec l'opérateur " * " 
resultat = nb1 * nb2;
console.log('La Multiplication de nb1 et nb2 = ' + resultat);

// #### ~ La Division ~ #### //

// -- La Division de nb1 et nb2 avec l'opérateur " - " 
resultat = nb1 / nb2;
console.log('La Division de nb1 et nb2 = ' + resultat);

// #### ~ Le Modulo ~ #### //

/**
 * Le Modulo correspond aureste de la division
 * Le Modulo de nb1 et nb2 avec l'opérateur " % "
 */

// -- Le Modulo de nb1 et nb2 avec l'opérateur " % " 
nb1 = 11;
resultat = nb1 % nb2;
console.log('Le Modulo de nb1 et nb2 = ' + resultat);


/* ----- LES ECRITURES SIMPLIFIEES ---- */

nb1 = 15;
nb1 = nb1 + 5;
nb1 += 5; // Ce qui équivaut à écrire nb1 = nb1 + 5;

